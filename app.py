import streamlit as st
import math

# -------------------------------------------------
# Настройки страницы
# -------------------------------------------------
st.set_page_config(
    page_title="Прогноз рецидива лейомиомы",
    layout="centered"
)

st.title("📊 Прогнозирование рецидива лейомиомы")
st.markdown(
    """
    Расчет вероятности рецидива лейомиомы на основе логистической регрессионной модели.
    
    **Cut-off согласно ROC-анализу: 0.25**
    """
)

st.divider()

# -------------------------------------------------
# Ввод данных
# -------------------------------------------------
st.subheader("Введите данные пациентки:")

col1, col2 = st.columns(2)

with col1:
    rody = st.radio("Роды в анамнезе", ["Да", "Нет"])
    dlit = st.radio("Длительность заболевания > 7 лет", ["Да", "Нет"])
    nzo = st.radio("Нарушение жирового обмена (ИМТ > 25)", ["Да", "Нет"])

with col2:
    amk = st.radio("АМК в анамнезе", ["Да", "Нет"])
    vozrast = st.number_input("Возраст (лет)", 18, 70, 35)
    razmer = st.number_input("Размер наибольшего узла (см)", 0.0, 20.0, 3.0)

# -------------------------------------------------
# Кодирование переменных (как в логистической модели)
# 1 = Да, 0 = Нет
# -------------------------------------------------
X_rody = 1 if rody == "Да" else 0
X_dlit = 1 if dlit == "Да" else 0
X_nzo = 1 if nzo == "Да" else 0
X_amk = 1 if amk == "Да" else 0

# -------------------------------------------------
# Расчет
# -------------------------------------------------
if st.button("Рассчитать вероятность рецидива"):

    # Логистическая функция (из диссертации)
    Z = (
        -2.8
        - 1.27 * X_rody
        + 1.57 * X_dlit
        + 0.72 * X_nzo
        + 1.06 * X_amk
        - 0.09 * vozrast
        - 0.3 * razmer
    )

    P = 1 / (1 + math.exp(-Z))   # вероятность от 0 до 1

    st.divider()
    st.subheader("Результат расчета")

    st.metric("Вероятность рецидива", f"{P:.3f}")

    st.write(f"Значение логистической функции (Z): {Z:.3f}")

    # -------------------------------------------------
    # Классификация риска (cut-off = 0.25)
    # -------------------------------------------------
    cutoff = 0.25

    if P > cutoff:
        st.error("🔴 Высокий риск рецидива (P > 0.25)")
    else:
        st.success("🟢 Низкий риск рецидива (P ≤ 0.25)")

    # -------------------------------------------------
    # Визуальная шкала риска
    # -------------------------------------------------
    st.progress(min(P, 1.0))

    st.caption(
        """
        Порог классификации выбран согласно ROC-анализу (cut-off = 0.25).
        AUC модели = 0.79 (95% ДИ: 0.68–0.90).
        
        Модель носит исследовательский характер и не заменяет клиническое решение врача.
        """
    )
